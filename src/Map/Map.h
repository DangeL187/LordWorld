	void loadMap() {
		std::string line;
        int index = 0;
        std::ifstream file;
        file.open("map.txt");
        if (file.is_open()) {
            while (file) {
                std::getline(file, line);
                std::vector<std::string> smap = split(line, ' ');
                for (int i = 0; i < smap.size()-1; i++) {
                    this->setTileMapID(index, i, std::stoi(smap[i]));
                }
                index++;
            }
        }
        file.close();
	}
public:
	Map() {
		loadMap();
	}

